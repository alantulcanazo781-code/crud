    public void mostrarMensaje(String mensaje) {
        JOptionPane.showMessageDialog(this, mensaje);
    }
    
    /**
     * Limpia todos los campos del formulario.
     */
    public void limpiarCampos() {
        txtidUsuario.setText("");
        txtNombres.setText("");
        txtApellidos.setText("");
        txtCedula.setText("");
        txtEdad.setText("");
        txtEmail.setText("");
    }
    
    /**
     * Conecta los botones (JButton) de esta Vista con los métodos del Controlador.Esta es la función CLAVE que une la Vista con el Controlador.
     * @param controlador
     */
    public void agregarListeners(ControladorUsuario controlador) {
        btnCrear.addActionListener(e -> controlador.crearUsuario());
        btnActualizar.addActionListener(e -> controlador.actualizarUsuario());
        btnBuscar.addActionListener(e -> controlador.buscarUsuario());
        btnBorrar.addActionListener(e -> controlador.eliminarUsuario());
        btnLimpiar.addActionListener(e -> limpiarCampos()); // Limpiar también usa un listener para su botón
    }
    
