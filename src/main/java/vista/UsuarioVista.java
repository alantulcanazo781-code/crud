    // --- SETTERS (Para que el Controlador cargue los datos de la BD al formulario) ---
    public void setCampos(int id, String nombres, String apellidos, String cedula, int edad, String email) {
        txtidUsuario.setText(String.valueOf(id));
        txtNombres.setText(nombres);
        txtApellidos.setText(apellidos);
        txtCedula.setText(cedula);
        txtEdad.setText(String.valueOf(edad));
        txtEmail.setText(email);
    }
    
